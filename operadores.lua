-- OPERADORES MATEMÁTICOS

Num01 = 10
Num02 = 15

Soma = Num01 + Num02
Subtracao = Num01 - Num02
Multiplicacao = Num01 * Num02
Divisao = Num01 / Num02
Resto = Num01 % Num02
Expo = Num01 ^ Num02

print(Soma)
print(Subtracao)
print(Multiplicacao)
print(Divisao)
print(Resto)
print(Expo)

print("\n")

-- OPERADORES RELACIONAIS

Menor = Num01 < Num02
Maior = Num01 > Num02
Menor_ou_Igual = Num01 <= Num02
Maior_ou_Igual = Num01 >= Num02
Igual = Num01 == Num02
Diferente = Num01 ~= Num02

print(Menor)
print(Maior)
print(Menor_ou_Igual)
print(Maior_ou_Igual)
print(Igual)
print(Diferente)

print("\n")

-- LÓGICOS
-- and: retorna o primeiro valor se for false, se não for retorna o segundo
-- or: retorna o primeiro se não for false, se for retorna o segundo
print(Num01 and Num02)
print(Num01 or Num02)
print(Num01 and not Num02)
